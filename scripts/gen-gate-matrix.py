import sys
import tempfile

HERE = pathlib.Path(__file__).resolve().parent.parent
sys.path.insert(0, str(HERE / "reference"))
from semantics import evaluate_text  # noqa: E402

STATES = ["success", "failure", "skipped", "cancelled"]
FORMS = [
    "with-value", "with-status", "with-error",
    "after-succeeded", "after-failed", "after-skipped", "after-terminal",
    "when-true", "when-false", "no-edge",
]
# Statically-dead cells (03 §static liveness): the producer's reachable
# state set never intersects the edge's pass-set — NIKA-DAG-006 territory.
DEAD_CELLS = {
    ("success", "after-skipped"),
    ("failure", "after-skipped"),
    ("cancelled", "after-skipped"),
    ("skipped", "after-succeeded"),
    ("skipped", "after-failed"),
}

RUNTIME_DIR = HERE / "conformance" / "tests" / "runtime" / "gates"
DEEP_DIR = HERE / "conformance" / "tests" / "deep"


def producer_yaml(state: str) -> str:
    """seed → p scaffold; p settles exactly `state` at runtime while keeping
    every OTHER edge form statically live (03 §static liveness)."""
    seed_cmd = '["false"]' if state == "cancelled" else '["true"]'
    lines = [
        "  seed:",
        f"    exec: {{ command: {seed_cmd} }}",
        "  p:",
        "    after: { seed: succeeded }",
    ]
    if state == "skipped":
        lines.append("    when: false")
    p_cmd = '["false"]' if state == "failure" else '["echo", "the producer speaks"]'
    lines.append(f"    exec: {{ command: {p_cmd} }}")
    return "\n".join(lines)


def consumer_yaml(form: str) -> str:
    body = {
        "with-value": [
            '    with: { v: "${{ tasks.p.output }}" }',
            '    exec: { command: ["echo", "got ${{ with.v }}"] }',
        ],
        "with-status": [
            '    with: { s: "${{ tasks.p.status }}" }',
            '    exec: { command: ["echo", "p was ${{ with.s }}"] }',
        ],
        "with-error": [
            '    with: { e: "${{ tasks.p.error }}" }',
            '    exec: { command: ["echo", "observation carried"] }',
        ],
        "after-succeeded": [
            "    after: { p: succeeded }",
            '    exec: { command: ["echo", "strict path"] }',
        ],
        "after-failed": [
            "    after: { p: failed }",
            '    exec: { command: ["echo", "failure path"] }',
        ],
        "after-skipped": [
            "    after: { p: skipped }",
            '    exec: { command: ["echo", "skip path"] }',
        ],
        "after-terminal": [
            "    after: { p: terminal }",
            '    exec: { command: ["echo", "always path"] }',
        ],
        "when-true": [
            "    when: true",
            '    exec: { command: ["echo", "independent"] }',
        ],
        "when-false": [
            "    when: false",
            '    exec: { command: ["echo", "never spoken"] }',
        ],
        "no-edge": [
            '    exec: { command: ["echo", "independent"] }',
        ],
    }[form]
    return "\n".join(["  c:"] + body)


def cell_workflow(state: str, form: str) -> str:
    return "\n".join([
        "# SPDX-License-Identifier: Apache-2.0",
        f"# gate-v2 matrix cell · producer settles {state} × consumer {form}",
        "# generated by scripts/gen-gate-matrix.py — edit the generator, not this",
        "nika: v1",
        "workflow:",
        f"  id: gate-{state}-x-{form}",
        "tasks:",
        producer_yaml(state),
        consumer_yaml(form),
        "",
    ])


# Deterministic side observations the pass-set semantics PIN (asserted on
# top of the model's statuses — the terminal-observation contract in bytes).
OUTPUT_CONTAINS = {
    ("success", "with-status"): ("c", "p was success"),
    ("failure", "with-status"): ("c", "p was failure"),
    ("skipped", "with-status"): ("c", "p was skipped"),
    ("cancelled", "with-status"): ("c", "p was cancelled"),
    ("success", "with-value"): ("c", "got the producer speaks"),
}


def expected_run(state: str, form: str, text: str) -> dict:
    model = evaluate_text(text)
    statuses = {tid: v["status"] for tid, v in model.items()}
    workflow_state = "failure" if any(
        v["status"] == "failure" and not v["recovered"] for v in model.values()
    ) else "success"
    tasks: dict[str, dict] = {tid: {"status": st} for tid, st in sorted(statuses.items())}
    hit = OUTPUT_CONTAINS.get((state, form))
    if hit and statuses.get(hit[0]) == "success":
        tasks[hit[0]]["output_contains"] = hit[1]
    return {
        "workflow_state": workflow_state,
        "tasks": tasks,
        "note": f"gate-v2 matrix · {state} × {form} · statuses authored by "
                "reference/semantics.py (the model defines · the engine follows)",
    }


def dead_cell_fixture(state: str, form: str) -> tuple[str, dict]:
    text = cell_workflow(state, form)
    expected = {
        "valid": False,
        "errors": [{"code": "NIKA-DAG-006", "category": "validation_error"}],
        "note": f"03-dag.md §static liveness · gate-v2 matrix cell {state} × "
                f"{form}: the edge's pass-set can never admit — the producer's "
                "reachable states exclude it · statically dead, refused before "
                "any run.",
    }
    return text, expected


