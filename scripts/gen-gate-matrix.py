"""Gate-v2 observation matrix — model-authored, engine-proven.

The COMPLETE admission matrix of 03 §gate algebra v2: producer terminal
state {success · failure · skipped · cancelled} × consumer edge form
{with-value · with-status · with-error · after:succeeded · after:failed ·
after:skipped · after:terminal · when-true · when-false · no-edge} =
40 cells. Every cell is a PERMANENT fixture:

- 35 cells are runtime observations → `conformance/tests/runtime/gates/`
  (input.nika.yaml + run.json + expected-run.json). Expected statuses come
  from `reference/semantics.py` — the model DEFINES the outcome, this
  script never hand-writes one.
- 5 cells are STATICALLY DEAD (03 §static liveness): the edge's pass-set
  excludes every reachable producer state → `conformance/tests/deep/`
  fixtures expecting NIKA-DAG-006 (check-only tier · the check refuses
  them before any run — that refusal IS the cell's semantics).
- plus one NIKA-DAG-007 vocabulary fixture (the bad-status-literal class
  that guards the observation half of the matrix).

usage:
  python3 scripts/gen-gate-matrix.py --write        # (re)generate fixtures
  python3 scripts/gen-gate-matrix.py --check        # drift gate (temp + diff)
  python3 scripts/gen-gate-matrix.py --prove BIN    # run the 35 at a binary
"""

from __future__ import annotations

import argparse
import filecmp
import json
import pathlib
import shutil
